extern "C" {

const oled_icon* oled_get_icon_data(oled_icon_id id)
{
	switch(id)
	{
		case OLED_ICON_OK:
			return &icon_ok;

		case OLED_ICON_YES:
			return &icon_yes;

		case OLED_ICON_NO:
			return &icon_no;

		case OLED_ICON_HOME:
			return &icon_home;

		case OLED_ICON_UP:
			return &icon_up;

		case OLED_ICON_DOWN:
			return &icon_down;

		case OLED_ICON_LEFT:
			return &icon_left;

		case OLED_ICON_RIGHT:
			return &icon_right;

		case OLED_ICON_WARNING:
			return &icon_warning;

		case OLED_ICON_REBOOT:
			return &icon_reboot;

		case OLED_ICON_POWER_OFF:
			return &icon_power_off;

		case OLED_ICON_SCREEN:
			return &icon_screen;

		default:
			return NULL;
	}
}

} // extern "C"

